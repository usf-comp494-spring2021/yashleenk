namespace HW3
{

    public class Calc
    {
        /// <summary>
        /// 
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public double add(double a, double b)
        {
            return a + b;
        }

        public double sub(double a, double b)
        {
            return a - b;
        }

        public double mult(double a, double b)
        {
            return a * b;
        }

        public double divi(double a, double b)
        {
            return a / b;
        }
    }
}
